    private void classIdCItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_classIdCItemStateChanged
        try {
            String classid = String.valueOf(classIdC.getSelectedItem());
            ClassS searchClass = classController.searchClass(classid);
            amountTE.setText(String.valueOf(searchClass.getClassFeesAmount()));
            yearCE.setSelectedItem(String.valueOf(searchClass.getYear()));
            gradeCE.setSelectedIndex(searchClass.getGrade()-3);
            dateCE.setSelectedItem(searchClass.getConductedDate());
// TODO add your handling code here:
        } catch (RemoteException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ClassDetail.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_classIdCItemStateChanged

    private void setClassDetailID() {
        try {
            int classCount = classController.getClassCount() + 1;
            if (classCount < 10) {
                classIdT.setText("CL00" + String.valueOf(classCount));
            } else if (classCount < 100) {
                classIdT.setText("CL0" + String.valueOf(classCount));
            } else {
                classIdT.setText("CL" + String.valueOf(classCount));
            }
        } catch (RemoteException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ClassDetail.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadClassCombo() {
        classIdC.removeAllItems();
        try {
            ArrayList<ClassS> allClass = classController.getAllClass();
            for (ClassS cls : allClass) {
                classIdC.addItem(cls.getClassId());
            }
        } catch (RemoteException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(StudentMarkManagement.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

