    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed

        String classId = classIdT.getText();
        int grade = gradeCA.getSelectedIndex() + 3;
        int year = Integer.parseInt(String.valueOf(yearCA.getSelectedItem()));
        String date = String.valueOf(dateCA.getSelectedItem());
        int amount = Integer.parseInt(amountTA.getText());
        ClassS classS = new ClassS(classId, grade, date, amount, year);
        try {
            boolean addNewClass = classController.addNewClass(classS);
            if (addNewClass) {
                JOptionPane.showMessageDialog(this, "class is added succesfully");
                setClassDetailID();
                amountTA.setText("");
                loadClassCombo();

            } else {
                JOptionPane.showMessageDialog(this, "class is not added successfully");
            }
        } catch (RemoteException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ClassDetail.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addBActionPerformed

