    private void addUserBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserBActionPerformed
        try {
            String name=nameText.getText();
            String password=passwordField.getText();
            System.out.println(password);
            int ability = 2;
            if(adminRButton.isSelected()){
                ability=1;
            }else  if(normalUserPower.isSelected()){
                ability=2;
            }else if(lowerUserPower.isSelected()){
                ability=3;
            }
            User user=new User(name, password, ability);
            
            int added = userController.addNewUser(user);
            if(added>0){
                JOptionPane.showMessageDialog(this,"The user "+user+" was added successfully");
            }else{
                JOptionPane.showMessageDialog(this,"Failed to create new account");
            }
            this.dispose();
        } catch (RemoteException | SQLException | ClassNotFoundException ex) {
            Logger.getLogger(NewUserCreator.class.getName()).log(Level.SEVERE, null, ex);
        }

      
    }//GEN-LAST:event_addUserBActionPerformed

